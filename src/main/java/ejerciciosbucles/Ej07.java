package ejerciciosbucles;

public class Ej07 {

    public static void main(String[] args) {

        //Simplimente va sumando a j + 1 cada vez que el bucle da una vuelta
        // que seria un total de 4 vueltas es decir  j=1 +4 = 5 el resultado
        int j = 1;
        for (int i = -13; i <= -10; i++) {
            System.out.println(j);
            j++;
        }
        System.out.println(j);

    }

}
